# in e not in
# Python trabalha com Strings iteráveis ou seja dá para navegar por cada elemento dela

nome = "Luís"
print(nome[0])

# in: se aquele elemento estiver entre os elementos da string
print("-----IN-----")
print("L" in nome)
print("N" in nome)

# not in: se aquele elemento não estiver entre os elementos da string
print("-----NOT IN-----")
print("L" not in nome)
print("N" not in nome)